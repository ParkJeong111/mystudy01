package kr.co.teamd.mvc.dto;

public class RecenthostDTO {

	private int rhnum;
	private String mid;
	private String hname;
	private String rhdate;
	
	public int getRhnum() {
		return rhnum;
	}
	public void setRhnum(int rhnum) {
		this.rhnum = rhnum;
	}
	public String getMid() {
		return mid;
	}
	public void setMid(String mid) {
		this.mid = mid;
	}
	public String getHname() {
		return hname;
	}
	public void setHname(String hname) {
		this.hname = hname;
	}
	public String getRhdate() {
		return rhdate;
	}
	public void setRhdate(String rhdate) {
		this.rhdate = rhdate;
	}
	
	
	
}
